
# Introdução aos tipos de dados - tipo str(string)

"""

Python = Linguagem de programação
Tipagem → Dinâmica / Forte
str → string → texto
Strings são textos que estão dentro de aspas. 
→ Aspas simples ('') ou duplas ("")

"""

# Tipagem - Característica de uma linguagem de programação que se refere a como a linguagem lida com os tipos de dados.

# Tipos de tipagem:
# - Fraca
# - Dinâmica → A própria linguagem já detecta o tipo do dado informado.
# - Forte
# - Estática → É necessário informar o tipo de dado.


# Abertura e fechamento de algo → deve ser feita com o mesmo caractere.
print("Hello world")
print('Hello world')

# Escape
# → Como mostrar uma aspa dentro de uma string

print("Bom \"dia\"")

# Outra opção é inverter as aspas
print('Hello "world".')
print("Hello 'world'.x2")

# r 
# → utilizado em expressões regulares.

print(r"Hello \"world\" ")
