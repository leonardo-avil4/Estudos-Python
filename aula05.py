# Tipo bool (boolean)
# Retorna True (verdadeiro/sim) ou False (falso/não) ao questionar algo em um programa.
# Para o computador, ou é sim ou é não, e não existe um talvez.

# Os tipos de dados primitivos da programação são: string, int, float e bool.


# Existem vários operadores utilizados para "questionar".
# O operador "==" é um operador lógico utilizado para verificar se um valor é igual ao outro e se são do mesmo tipo.

print(10 == 10) # True
print(10 == 11) # False
print(type(10 == 5)) # <class 'bool'>

print(10 == '10')

