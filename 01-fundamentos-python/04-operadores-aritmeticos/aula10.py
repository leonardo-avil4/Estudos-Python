
# Concatenação (+) e repetição (*) com operadores aritmético.

# Peculiaridades de alguns operadores:

"""

 + em strings → concatena (junta) as strings
 + em int/float → efetua a soma.

 * em strings → repete a string N vezes.
 * em int/float → efetua a multiplicação.

"""

print('A' + 'B' + 'C') # ABC
print('A' * 5) # AAAAA
print()
