
# Tipos int e float (tipos primitivos)

# int → Define um número inteiro. 
# O tipo int representa qualquer número positivo ou negativo. 
# int sem sinal é considerado positivo.

# print(11) 
print(-11)
print(0)

# float → Número com ponto flutuante.
# O tipo float representa qualquer número positivo ou negativo com ponto flutuante
# . → É utilizado para separar casas decimais de um número.
# , → A vírgula é utilizada para separar dados/valores em chamadas de funções, por exemplo.

print(1.1, 10.11) 

# função type → mostra o tipo que o Python inferiu ao valor.

print(type('bom dia')) # <class 'str>
print(type(123)) # <class 'int'>

